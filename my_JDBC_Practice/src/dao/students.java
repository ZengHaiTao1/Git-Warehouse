package dao;

public class students {
	
	private String banji;
	private String name;
	/**
	 * @return banji
	 */
	public String getBanji() {
		return banji;
	}
	/**
	 * @param banji 要设置的 banji
	 */
	public void setBanji(String banji) {
		this.banji = banji;
	}
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name 要设置的 name
	 */
	public void setName(String name) {
		this.name = name;
	}
}
